    <h2 id="background"><a href="#table-of-content">Background</a></h2>
    <p>Now that I am starting my final semester as a Computer Science undergrad, I feel like I can share some of my personal tips to new CS majors who are starting their programs. These may not work for everyone, but these are some of the things I knew back when I first started as a freshman.</p>

    <h2 id="start-assignments-early"><a href="#table-of-content">Start assignments early</a></h2>

    <p>This is definitley the most important tip I can give anyone beginning their CS program! Like many, I always end up procrastinating when it comes to doing assignments or studying, especially for classes that are not intresting to me. This became more of an issue for me the tougher the classes became. For example, I could write up an assignment for my intro to C++ class in maybe an hour or two. However in my COBOL class, it would take me, at times, 30 hours to write a complete program! One time in my COBOL class, after not starting early on an assignment, I was forced to program till 6AM for three nights in a row. I quickly realized that I should be starting all assignments at least a week before they are due.</p>

    <p><b>TL;DR </b> Start your assignments at least <i>one week</i> before they are due.</p>


    <h2 id="program-in-teams"><a href="#table-of-content">Program in teams</a></h2>

    <p>When I came to college, I was not the type of person to talk to other students during class. It just wasn't my thing. So when a professor brought up how students were allowed to pair up with one another and work on assignmets in teams, I took no intrest in trying to find a partner. At first, your assignments may not seem difficult enough to find someone to do them with, but as the classes get harder you should ask yourself if having a second set of eyes could benefit you in your programming. Seldom do programmers work on an application on their own, so working with a partner can help set you on a path of knowing how to work with others when writing software. It can also help you and your partner get your homework done faster.</p>

    <p><b>TL;DR</b> Work with other students on assignments/projects if possible.</p>

    <h2 id="discord"><a href="#table-of-content">Discord</a></h2>

    <p>This is probably the second best advice I could give, and it's to either start or join a <a href="https://discordapp.com/" target="_blank">discord server</a> that has channels for every CS course offered at your university. There was one at NIU that was created in senior year, and it was probably one of the most helpful tools I could have asked for. Students could help eachother on code, and if it wasn't for the discord server I am not sure if I would have passed all of my classes.</p>

    <p><b>TL;DR</b> Start or join a discord server with channels for all the CS courses.</p>

